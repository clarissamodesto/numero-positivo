#Faça um programa que peça um valor e mostre na tela se o valor é positivo ou negativo

valor = float (input('Digite um número: '))

if valor > 0:
    print('Esse número é positivo')
elif valor == 0:
    print ('Esse número é zero')
else:
    print('Esse número é negativo')